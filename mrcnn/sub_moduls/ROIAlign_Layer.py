        # Re-add the batch dimension
        shape = tf.concat([tf.shape(boxes)[:2], tf.shape(pooled4)[1:]], axis=0)
        pooled4 = tf.reshape(pooled4, shape)
        # 5
        roi_level5 = log2_graph(tf.sqrt(h * w) / (224.0 / tf.sqrt(image_area)))
        roi_level5 = tf.minimum(5, tf.maximum(
            5, 6 + tf.cast(tf.round(roi_level5), tf.int32)))
        roi_level5 = tf.squeeze(roi_level5, 2)

